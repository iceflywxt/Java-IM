    /**
     * zkontroluje aktualni stav kontaktu a pokud je offline, zakaze odesilani zprav
     */
    public void checkContactState() {
        if (contact.isOnline()) {
            send.setEnabled(true);
        } else {
            send.setEnabled(false);
        }

        notice.setText(contact.isOnline() ? "" : contact.getName() + " je offline.");
    }

